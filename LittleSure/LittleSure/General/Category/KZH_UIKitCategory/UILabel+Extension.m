#import "UILabel+Extension.h"

@implementation UILabel (Extension)

- (void)getBlackType_14 {
    self.font = KZHFONT_14;
    self.textColor = KZH_Black;
}

- (void)getBlackType_12 {
    self.font = KZHFONT_12;
    self.textColor = KZH_Black;
}

- (void)getGrayType_12 {
    self.font = KZHFONT_12;
    self.textColor = KZH_Gray;
}

- (void)getGrayType_14 {
    self.font = KZHFONT_14;
    self.textColor = KZH_Gray;
}

- (void)getGrayType_16 {
    self.font = KZHFONT_16;
    self.textColor = KZH_Gray;
}

@end
